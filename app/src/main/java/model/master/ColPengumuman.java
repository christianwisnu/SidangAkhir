package model.master;

public class ColPengumuman {

    private Integer id;
    private String judul;
    private String isi;
    private String tglAwal;
    private String tglFinish;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getJudul() {
        return judul;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public String getIsi() {
        return isi;
    }

    public void setIsi(String isi) {
        this.isi = isi;
    }

    public String getTglAwal() {
        return tglAwal;
    }

    public void setTglAwal(String tglAwal) {
        this.tglAwal = tglAwal;
    }

    public String getTglFinish() {
        return tglFinish;
    }

    public void setTglFinish(String tglFinish) {
        this.tglFinish = tglFinish;
    }
}

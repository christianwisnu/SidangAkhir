package model.master;

public class ColSidang {

    private Integer id;
    private String judulSidang;
    private String nbiMhs;
    private String namaMhs;
    private String cdstatus;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getJudulSidang() {
        return judulSidang;
    }

    public void setJudulSidang(String judulSidang) {
        this.judulSidang = judulSidang;
    }

    public String getNbiMhs() {
        return nbiMhs;
    }

    public void setNbiMhs(String nbiMhs) {
        this.nbiMhs = nbiMhs;
    }

    public String getNamaMhs() {
        return namaMhs;
    }

    public void setNamaMhs(String namaMhs) {
        this.namaMhs = namaMhs;
    }

    public String getCdstatus() {
        return cdstatus;
    }

    public void setCdstatus(String cdstatus) {
        this.cdstatus = cdstatus;
    }
}
